package niagaCanvas;

import javax.swing.SwingUtilities;

public final class GUILauncher {

	/** Utility class. */
	private GUILauncher() {
	}

	/**
	 * Starts the GUI.
	 * 
	 * @param args
	 *            Not used.
	 * @throws Exception
	 *             if something goes wrong
	 */
	public static void main(final String[] args) throws Exception {
		SwingUtilities.invokeLater(() -> {
			new NiaGUI();
		});
	}
}
